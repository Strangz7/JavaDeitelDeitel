package personal;

public class Motherboard {
    private String size;
    private String length;

    public Motherboard(String size, String length) {
        this.size = size;
        this.length = length;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getLength() {
        return length;
    }

    public void setLength(String length) {
        this.length = length;
    }


}
